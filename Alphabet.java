class Alphabet
{
	public static void main(String[] args)
	{
		char c1='a';
		char c2='g';
		int c3=5;
		
		if((c1>='A' && c1<='Z') || (c1>='a' && c1<='z'))
			System.out.println(c1+"is a alphabet");
		else
		    System.out.println(c1+"is not alphabet");
		
		
		if((c2>='A' && c2<='Z') || (c2>='a' && c2<='z'))
			System.out.println(c2+"is a alphabet");
		else
		    System.out.println(c2+"is not alphabet");
		
		
		if((c3>='A' && c3<='Z') || (c3>='a' && c3<='z'))
			System.out.println(c3+"is a alphabet");
		else
		    System.out.println(c3+"is not alphabet");
		
		
		
	}
	
	
	
	
	
	
	
}