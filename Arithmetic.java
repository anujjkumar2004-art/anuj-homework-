class Arithmetic
{
	public static void main(String[] args)
	{
		int a=20;
		int b=6;
		int c=a+b;
		int d=a-b;
		int e=a/b;
		int f=a%b;
		
		System.out.println("Addition=" + c);
		System.out.println("Subtraction=" + d);
		System.out.println("Division=" + e);
		System.out.println("Remainder=" + f);
		
	}
	
	
	
	
	
}